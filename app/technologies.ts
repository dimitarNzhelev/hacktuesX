interface TechnologyDictionary {
  [key: number]: { name: string; color: string };
}

export function getTechnologyInfoByName(
  technologyName: string,
): { id: number; color: string } | null {
  for (const id in technologies) {
    if (technologies[id].name === technologyName) {
      return { id: parseInt(id), color: technologies[id].color };
    }
  }
  return null;
}

